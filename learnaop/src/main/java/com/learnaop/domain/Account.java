package com.learnaop.domain;

import java.io.Serial;
import java.io.Serializable;

//对象序列化  DataoutputStream writeInt  writeUTF
public class Account implements Serializable {


    @Serial
    private static final long serialVersionUID = -2737150044958652720L;

    private Integer id;
    private String name;
    private Double money;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getMoney() {
        return money;
    }

    public void setMoney(Double money) {
        this.money = money;
    }

    @Override
    public String toString() {
        return "Account{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", money=" + money +
                '}';
    }
}
